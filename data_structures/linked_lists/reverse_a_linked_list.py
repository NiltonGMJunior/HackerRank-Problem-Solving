class SinglyLinkedListNode:
    def __init__(self, node_data):
        self.data = node_data
        self.next = None

class SinglyLinkedList:
    def __init__(self):
        self.head = None
        self.tail = None

    def insert_node(self, node_data):
        node = SinglyLinkedListNode(node_data)

        if not self.head:
            self.head = node
        else:
            self.tail.next = node


        self.tail = node

def print_singly_linked_list(node, sep, fptr):
    while node:
        fptr.write(str(node.data))

        node = node.next

        if node:
            fptr.write(sep)

def reverse(head):
    if head is None:
        return None

    llist_nodes = [head]

    cur = head
    while cur.next:
        llist_nodes.append(cur.next)
        cur = cur.next
    
    reverse_llist = llist_nodes[::-1]
    for index, node in enumerate(reverse_llist[:-1]):
        node.next = reverse_llist[index + 1]
    
    llist_nodes[0].next = None

    return llist_nodes[-1]


